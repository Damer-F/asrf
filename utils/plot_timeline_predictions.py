import argparse
import glob
import os
from typing import Dict, List, Optional, Sequence

import matplotlib.pyplot as plt
import numpy as np
from matplotlib.colors import BoundaryNorm, ListedColormap
from matplotlib.patches import Patch


def get_arguments() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Plot GT, before-refinement prediction, refined prediction, and boundary as aligned timelines."
    )
    parser.add_argument(
        "dir",
        type=str,
        help="path to a prediction array directory generated by save_pred.py",
    )
    parser.add_argument(
        "--output_dir",
        type=str,
        default=None,
        help="directory where timeline images will be saved. Defaults to sibling predictions/ for prediction_arrays/ inputs.",
    )
    parser.add_argument(
        "--dataset",
        type=str,
        default=None,
        help="dataset name used to read mapping.txt, e.g. 50salads, gtea, breakfast",
    )
    parser.add_argument(
        "--dataset_dir",
        type=str,
        default="./dataset",
        help="dataset root directory containing mapping.txt",
    )
    parser.add_argument(
        "--names",
        nargs="*",
        default=None,
        help="optional video base names to plot, e.g. rgb-01-1 rgb-01-2",
    )
    parser.add_argument(
        "--boundary_th",
        type=float,
        default=0.5,
        help="boundary probability threshold shown on the boundary row",
    )
    parser.add_argument(
        "--dpi",
        type=int,
        default=180,
        help="output image dpi",
    )
    return parser.parse_args()


def load_id2class(dataset: Optional[str], dataset_dir: str) -> Dict[int, str]:
    if dataset is None:
        return {}

    mapping_path = os.path.join(dataset_dir, dataset, "mapping.txt")
    if not os.path.exists(mapping_path):
        return {}

    id2class = {}
    with open(mapping_path, "r") as f:
        for line in f:
            parts = line.strip().split(maxsplit=1)
            if len(parts) == 2:
                id2class[int(parts[0])] = parts[1]
    return id2class


def make_class_colormap(n_classes: int) -> ListedColormap:
    if n_classes <= 20:
        colors = plt.get_cmap("tab20").colors[:n_classes]
    else:
        colors = plt.get_cmap("hsv")(np.linspace(0, 1, n_classes, endpoint=False))
    return ListedColormap(colors)


def strip_invalid_padding(arr: np.ndarray) -> np.ndarray:
    return arr[arr != 255]


def load_optional_array(path: str) -> Optional[np.ndarray]:
    if os.path.exists(path):
        return np.load(path)
    return None


def find_video_names(pred_dir: str, names: Optional[Sequence[str]]) -> List[str]:
    if names:
        return list(names)

    gt_paths = sorted(glob.glob(os.path.join(pred_dir, "*_gt.npy")))
    return [os.path.basename(path)[: -len("_gt.npy")] for path in gt_paths]


def default_output_dir(array_dir: str) -> str:
    normalized = os.path.normpath(array_dir)
    parent = os.path.dirname(normalized)
    if os.path.basename(normalized) == "prediction_arrays":
        return os.path.join(parent, "predictions")
    return normalized


def plot_one_video(
    array_dir: str,
    output_dir: str,
    name: str,
    id2class: Dict[int, str],
    boundary_th: float,
    dpi: int,
) -> Optional[str]:
    gt_path = os.path.join(array_dir, name + "_gt.npy")
    pred_path = os.path.join(array_dir, name + "_pred.npy")
    refined_path = os.path.join(array_dir, name + "_refined_pred.npy")
    boundary_path = os.path.join(array_dir, name + "_boundary.npy")

    if not (
        os.path.exists(gt_path)
        and os.path.exists(pred_path)
        and os.path.exists(refined_path)
    ):
        print("skip {}: missing gt/pred/refined arrays".format(name))
        return None

    gt = strip_invalid_padding(np.load(gt_path).astype(np.int64))
    pred = np.load(pred_path).astype(np.int64)[: len(gt)]
    refined = np.load(refined_path).astype(np.int64)[: len(gt)]
    boundary = load_optional_array(boundary_path)
    if boundary is not None:
        boundary = boundary.astype(np.float32)[: len(gt)]

    max_class_id = int(max(gt.max(), pred.max(), refined.max()))
    cmap = make_class_colormap(max_class_id + 1)
    norm = BoundaryNorm(np.arange(max_class_id + 2) - 0.5, cmap.N)

    has_boundary = boundary is not None
    n_rows = 4 if has_boundary else 3
    height = 2.7 if has_boundary else 2.25
    fig, axes = plt.subplots(
        n_rows,
        1,
        figsize=(16, height),
        sharex=True,
        gridspec_kw={"height_ratios": [1, 1, 1, 0.6] if has_boundary else [1, 1, 1]},
        constrained_layout=True,
    )
    if n_rows == 1:
        axes = [axes]

    rows = [("GT", gt), ("Before refine", pred), ("Refine", refined)]
    for ax, (label, arr) in zip(axes[:3], rows):
        ax.imshow(
            arr[np.newaxis, :],
            aspect="auto",
            cmap=cmap,
            norm=norm,
            interpolation="nearest",
        )
        ax.set_yticks([])
        ax.set_ylabel(label, rotation=0, ha="right", va="center")
        ax.spines[["top", "right", "left"]].set_visible(False)

    if has_boundary:
        ax = axes[3]
        ax.imshow(
            boundary[np.newaxis, :],
            aspect="auto",
            cmap="YlOrRd",
            vmin=0.0,
            vmax=1.0,
            interpolation="nearest",
        )
        idx = np.where(boundary >= boundary_th)[0]
        if len(idx) > 0:
            ax.vlines(idx, -0.5, 0.5, colors="black", linewidth=0.25, alpha=0.35)
        ax.set_yticks([])
        ax.set_ylabel("Boundary", rotation=0, ha="right", va="center")
        ax.spines[["top", "right", "left"]].set_visible(False)

    axes[-1].set_xlabel("Frame index")
    fig.suptitle(name)

    used_ids = sorted(set(gt.tolist()) | set(pred.tolist()) | set(refined.tolist()))
    legend_handles = [
        Patch(
            facecolor=cmap(class_id),
            label="{} {}".format(class_id, id2class.get(class_id, "class_{}".format(class_id))),
        )
        for class_id in used_ids
    ]
    fig.legend(
        handles=legend_handles,
        loc="center left",
        bbox_to_anchor=(1.0, 0.5),
        frameon=False,
        fontsize="small",
    )

    os.makedirs(output_dir, exist_ok=True)
    out_path = os.path.join(output_dir, name + "_timeline.png")
    fig.savefig(out_path, dpi=dpi, bbox_inches="tight")
    plt.close(fig)
    return out_path


def main() -> None:
    args = get_arguments()
    id2class = load_id2class(args.dataset, args.dataset_dir)
    output_dir = args.output_dir if args.output_dir is not None else default_output_dir(args.dir)
    names = find_video_names(args.dir, args.names)

    out_paths = []
    for name in names:
        out_path = plot_one_video(
            args.dir,
            output_dir,
            name,
            id2class,
            boundary_th=args.boundary_th,
            dpi=args.dpi,
        )
        if out_path is not None:
            out_paths.append(out_path)

    print("saved {} timeline image(s)".format(len(out_paths)))


if __name__ == "__main__":
    main()
